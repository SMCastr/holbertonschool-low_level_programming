#include "3-calc.h"

/**
 * op_add - return sum
 * @a: int
 * @b: int
 * Return: always 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return difference
 * @a: int
 * @b: int
 * Return: always 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the product
 * @a: int
 * @b: int
 * Return: always 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return division
 * @a: int
 * @b: int
 * Return: always 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the reminder
 * @a: int
 * @b: int
 * Return: always 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
